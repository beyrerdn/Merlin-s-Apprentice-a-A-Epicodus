# Write a method that takes in two numbers. Return the greatest
# integer that evenly divides both numbers. You may wish to use the
# `%` modulo operation.
#
# Difficulty: medium.

def greatest_common_factor(number1, number2)
  greatest = 0
  n = 1
  while n < 100
    if (number1%n ==0) and (number2%n == 0)
      if n >= greatest
        greatest = n
      end
    end
    n += 1
  end
  return greatest
end

